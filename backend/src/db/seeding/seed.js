async function hashUserPassword(userData) {
  let newUserData = [];
  for (const user of userData) {
    const hashedPass = await bcrypt.hash(user.password, 10);
    newUserData.push({
      ...user,
      password: hashedPass,
    });
  }
  return newUserData;
}

